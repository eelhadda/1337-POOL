int	ft_is_prime(int nb)
{
	unsigned int	i;
	unsigned int	j;

	i = 2;
	j = nb;
	if (j < 2)
		return (0);
	while (i * i <= j)
	{
		if (j % i == 0)
			return (0);
		i++;
	}
	return (1);
}
